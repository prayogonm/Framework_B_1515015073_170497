	<font size="6" color="black" face="old school united">Tentang Sejarah Unmul <br> <br></font> <font size="4" color="black"  face="Times new roman">Provinsi Kalimantan Timur pada tahun 1960-an masih berupakan daerah yang sedikit penduduknya dan penyebarannya tidak merata, bahkan banyak daerah yang terisolir. Dari kota Balikpapan ke kota Samarinda saja harus ditempuh dalam waktu 1 hari melalui jalan darat dan sungai. Banyak putra daerah Kalimantan Timur yang melanjutkan pendidikan tinggi harus menuju Banjarmasin, Makassar atau kota-kota di pulau Jawa. Karena itulah, atas keinginan masyarakat Kalimantan Timur, direncanakanlah pendirian perguruan tinggi di Samarinda. Gubernur Abdoel Moeis Hasan kemudian meminta Bupati Soebrata Yoeda Soebrata mengadakan pertemuan dengan pemuka masyarakat di Samarinda dan kemudian berdasarkan keputusan Pemerintah Daerah Kalimantan Timur pada tanggal 7 Juni 1962[4] dibentuklah Perguruan Tinggi Mulawarman di Samarinda[5].

Demi menopang kebutuhan dana, fasilitas, dan peralatan Perguruan Tinggi Mulawarman, ditetapkan juga Yayasan Perguruan Tinggi Mulawarman yang dipimpin oleh Abdul Aziz Samad, didampingi sekertaris E. Abdul Samad dan bendahara Dorinawati Samalo (Ny. Lo Beng Long). Kemudian pada tanggal 28 Juni 1962 ditetapkan pula Presidium untuk menyelenggarakan Perguruan Tinggi Mulawarman[6] dengan Sekretaris Drs. Achmad Dahlan dan dibantu Husein Achutanair dan Syahidin, BA sebagai penyelenggara administrasi[7] yang berkoordinasi dengan Menteri Perguruan Tinggi dan Ilmu Pengetahuan di Jakarta. Kemudian dibentuk Panitia Persiapan Pendirian Perguruan Tinggi Mulawarman yang diketuai langsung oleh Gubernur Kalimantan Timur, Abdoel Moeis Hasan, dengan Ketua II Kolonel Soeharjo, Sekertaris II Drs. Achmad Dahlan (Departemen Pemerintahan Umum dan Otonomi Daerah) dan beranggotakan Pandji Adiamidjaja, SH (Kepala Wilayah Kejaksaan Tinggi Kalimantan Timur), Amir Datuk Palindih (Kepala Polisi Komisariat Kalimantan Timur), Letnan Kolonel Ngoedio, Bc.HK (Walikota Samarinda), Sayid Mochsen (Bupati Kepala Daerah Tingkat II Kalimantan Timur), Drs. Anwar Chanani (Departemen Pemerintahan Umum dan Otonomi Daerah), Drs. Soejitno (Departemen Pemerintahan Umum dan Otonomi Daerah), M. Saleh Nafsi, SH (Departemen Pemerintahan Umum dan Otonomi Daerah), Ir. Gazaly (Kepala Planologi dan Perhutanan di Samarinda), Ir. Ismet Hakim (Jawatan Pertanian), Soetopo, SH (Kehakiman), dan Ny. Djumantan Anang Hasyim. Pengurus Perguruan Tinggi Mulawarman kemudian ditetapkan dengan Ketua Sayid Mochsen dan dibantu Letnan Kolonel Ngoedio, Bc.HK sebagai Wakil Ketua, A. Sanie Rachman sebagai Sekertaris I, Syahidin, BA sebagai Sekertaris II dan beranggotakan M. Saleh Nafsi, SH, Ny. Djoemantan Anang Hasyim, Drs. Anwar Chanani, Ir. Gazaly, Ir. Ismet Hakim, dan Aji Bambang Malik[5].

Perguruan Tinggi Mulawarman kemudian ditetapkan sebagai Universitas Kalimantan Timur berdasarkan keputusan Menteri Pendidikan Tinggi dan Ilmu Pengetahuan Thoyib Hadiwidjaja[8] pada tanggal 28 September 1962. Namun nama ini dikembalikan menjadi Universitas Mulawarman dan dikukuhkan secara resmi oleh Presiden Republik Indonesia Soekarno[9] pada tanggal 23 April 1963. Tanggal 27 September 1962 kemudian ditetapkan sebagai tanggal berdirinya Universitas Mulawarman.</font><br>
